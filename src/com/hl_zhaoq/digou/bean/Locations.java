package com.hl_zhaoq.digou.bean;

public class Locations {
	public String myStreet;
	public String myProvince;
	public String myCity;
	public String myDistrict;
	public double myLongitude;
	public double myLatitude;
	public String getMyStreet() {
		return myStreet;
	}
	public void setMyStreet(String myStreet) {
		this.myStreet = myStreet;
	}
	public String getMyProvince() {
		return myProvince;
	}
	public void setMyProvince(String myProvince) {
		this.myProvince = myProvince;
	}
	public String getMyCity() {
		return myCity;
	}
	public void setMyCity(String myCity) {
		this.myCity = myCity;
	}
	public String getMyDistrict() {
		return myDistrict;
	}
	public void setMyDistrict(String myDistrict) {
		this.myDistrict = myDistrict;
	}
	public double getMyLongitude() {
		return myLongitude;
	}
	public void setMyLongitude(double myLongitude) {
		this.myLongitude = myLongitude;
	}
	public double getMyLatitude() {
		return myLatitude;
	}
	public void setMyLatitude(double myLatitude) {
		this.myLatitude = myLatitude;
	}
}
