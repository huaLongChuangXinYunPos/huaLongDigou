package com.hlcxdg.digou.bean.newbean;

public class PhoneInfo {
	public String APPVersion;
	public String HardWareID;
	public String OSName;
	public String OSVerdion;
	public double Longitude;
	public double Latitude;
	public String WIFIorXG;

	public String getAPPVersion() {
		return APPVersion;
	}

	public void setAPPVersion(String aPPVersion) {
		APPVersion = aPPVersion;
	}

	public String getHardWareID() {
		return HardWareID;
	}

	public void setHardWareID(String hardWareID) {
		HardWareID = hardWareID;
	}

	public String getOSName() {
		return OSName;
	}

	public void setOSName(String oSName) {
		OSName = oSName;
	}

	public String getOSVerdion() {
		return OSVerdion;
	}

	public void setOSVerdion(String oSVerdion) {
		OSVerdion = oSVerdion;
	}

	public double getLongitude() {
		return Longitude;
	}

	public void setLongitude(double longitude) {
		Longitude = longitude;
	}

	public double getLatitude() {
		return Latitude;
	}

	public void setLatitude(double latitude) {
		Latitude = latitude;
	}

	public String getWIFIorXG() {
		return WIFIorXG;
	}

	public void setWIFIorXG(String wIFIorXG) {
		WIFIorXG = wIFIorXG;
	}

}
